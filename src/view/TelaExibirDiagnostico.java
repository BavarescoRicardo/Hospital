    public void attTabela(){
        for (Diagnostico diag : diagnosticoDao.listar()){
            if (idProntuario == diag.getProntuario().getIdProntuario())
                dtm.addRow(new Object[]{diag.getIdDiagnostico(),diag.getProntuario().getPaciente().getNOME(),diag.getDoenca().getNome(),diag.getSTATUS()});
        }
        
    }

