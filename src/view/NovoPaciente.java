    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // criar objeto paciente 
        String nome = txtNome.getText();
        java.sql.Date dataNasc = new java.sql.Date(dataEscolhida.getDate().getTime());
        String cpf = txtCpf.getText();
        String email = txtEmail.getText();
        Paciente paciente = new Paciente(nome, cpf, email, dataNasc);
        
        // salvar paciente no banco de dados
        PacienteDao dao = new  PacienteDao();
        dao.salvar(paciente);
        
        // limpar os campos de texto
        txtNome.setText("");
        txtEmail.setText("");
        txtCpf.setText("");
        
        // mostrar mensagem de confirma√ßao
        JOptionPane.showMessageDialog(rootPane, " Paciente salvo com sucesso !");
    }//GEN-LAST:event_jButton1ActionPerformed


